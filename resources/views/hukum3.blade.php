                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                        Jakarta - Direktorat Tindak Pidana Siber (Dittipidsiber) Bareskrim Polri <br> membongkar kasus ilegal akses dan penyebaran data elektronik milik Badan <br> Kepegawaian Negara (BKN) pada suatu situs.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">   
                        Dalam konferensi pers di Gedung Bareskrim Polri, Jakarta, Selasa, Direktur Tindak Pidana <br> Siber (Dirtipidsiber) Bareskrim Polri Brigjen Pol Himawan Bayu Aji mengatakan bahwa <br> pengungkapan itu merupakan  hasil koordinasi pihaknya bersama Badan Siber <br> dan Sandi Negara (BSSN) usai mengetahui adanya insiden siber pada sistem elektronik milik BKN.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                        Hasil penyidikan pun menunjukkan bahwa tersangka memiliki inisial BAG (25) dan <br>merupakan seorang guru honorer di Banyuwangi, Jawa Timur. Tersangka BAG diketahui <br> melakukan ilegal akses terhadap situs dengan salah satu akun milik pegawai BKN.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                        "Pelaku mendapatkan login akses milik admin Satu Data ASN dari salah satu forum di <br> breachforums.st," ungkap Himawan.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                        Pada forum tersebut, kata dia, ditemukan banyak akun username dan kata sandi sistem <br>elektronik dari seluruh dunia, baik yang masih aktif ataupun yang sudah kedaluwarsa.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                        Kemudian, tersangka mengunduh data dari situs BKN dengan total 6,3 GB dan <br> menyebarkannya dengan menjualnya melalui situs breachforums.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;"> 
                        Selain data elektronik BKN, tersangka juga melakukan penyebaran data 40 sistem <br> elektronik lainnya, di antaranya milik salah satu universitas di Amerika dan perusahaan <br> swasta di Amerika, Taiwan, Belgia, Inggris, Thailand, Afrika Selatan, India, dan Hong Kong.
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;"> 
                        Adapun tersangka menjual data tersebut melalui breachforums.st untuk keuntungan <br> pribadi.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;"> 
                        "Tersangka mendapatkan keuntungan sejumlah 8.000 dolar AS dari hasil penjualan data- <br>data tersebut," ucap Himawan.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;"> 
                        Dalam penangkapan tersangka, penyidik turut mengamankan sejumlah barang bukti, <br> di antaranya dua laptop (komputer jinjing), dua ponsel, dan satu buah motor yang <br>dibeli dari hasil penjualan data.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;"> 
                        Tersangka BAG dijerat dengan UU Perlindungan Data Pribadi, Informasi dan Transaksi <br> Elektronik, dan Pencegahan dan Pemberantasan Tindak Pidana Pencucian Uang, serta <br> KUHP dengan sanksi pidana penjara paling lama 10 tahun penjara.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;"> 
