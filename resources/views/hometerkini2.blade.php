                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                        Jakarta - Direktur Jenderal Bina Pemerintahan Desa Kementerian Dalam <br>Negeri La Ode Ahmad P. Bolombo berharap Lomba Desa dan Kelurahan 2024 yang <br>
                        bertajuk "Mewujudkan Masyarakat Sejahtera Melalui Belanja Desa dan Kelurahan yang <br> Berkualitas" dapat berkontribusi terhadap pencapaian target prioritas nasional terkait <br> ketahanan ekonomi untuk pertumbuhan berkualitas dan berkeadilan.
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                        Hal itu disampaikan La Ode dalam pemaparan calon juara Lomba Desa Tingkat Regional <br>
                        Tahun 2024 di Ruang Paramananda Kantor Ditjen Bina Pemdes, Jakarta, Selasa (17/9).
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                      "Besar harapan saya bahwa penjaringan desa dan kelurahan berprestasi tingkat nasional <br>
                       tahun 2024 ini dapat terlaksana, dengan tetap menjaga integritas, netralitas, dan <br>
                       sportifitas dan akan menghasilkan desa dan kelurahan berprestasi yang akan menjadi <br> percontohan bagi desa dan kelurahan lain di seluruh Indonesia," kata La Ode dalam <br>
                       keterangannya di Jakarta, Jumat.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                        Dia juga menambahkan bahwa Kepala Desa dan Lurah Juara Lomba Tingkat Regional <br> serta Juara Evaluasi Desa Teladan PKAD akan diundang pada kegiatan pengembangan <br> motivasi yang rencananya dilaksanakan di bali pada akhir Oktober.
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                        Sementara itu, Direktur Pengembangan Kapasitas Pemerintahan Desa, Data Dan <br> Evaluasi Perkembangan Desa Mohammad Noval mengatakan pemaparan ini merupakan <br> bagian dari rangkaian kegiatan lomba yang bertujuan untuk mendapatkan informasi <br> yang valid desa dan kelurahan.
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                       Guru Besar UIN Syarif Hidayatullah Jakarta Amilin sebagai dewan juri berharap <br> kepada desa yang menjadi juara untuk tidak berpuas diri dan tetap melanjutkan apa yang <br> menjadi keunggulan di daerahnya.                    
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                        "Lomba Desa dan Kelurahan 2024 kali ini sangat kompetitif. Harapan saya bagi semua <br> desa yang ikut lomba, tidak boleh berpuas diri, sehingga tetap berlanjut <br> mengembangkan potensi dan menjadi role model bagi seluruh Desa yang ada di <br> Indonesia," ujar Amilin.                     
                    </h4> 
                     <h4 style="margin-left: 16%; padding-top: 3%;">      
                        Adapun Lomba Desa dan Kelurahan 2024 ini menjadi salah satu kegiatan yang <br> mendapatkan support dari Program P3PD khususnya keterkaitan dengan Project <br> Development Objective (PDO) P3PD.                     
                    </h4> 
                    <h4 style="margin-left: 16%; padding-top: 3%;">      
                       "Maksud dan tujuan pelaksanaan pemaparan calon juara lomba adalah mendapatkan <br> informasi yang valid atau kondisi real desa/kelurahan yang disampaikan langsung oleh <br> desa/kelurahan bersangkutan untuk diuji dan dipastikan kebenarannya. Di mana <br>  <br>selanjutnya dapat menentukan peringkat I, II & III tingkat nasional di tiap regional," <br> jelasnya.      
                   </h4> 
